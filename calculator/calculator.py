def main():
    first_num = int(input("Input first number: "))
    second_num = int(input("Input second number: "))

    print(f"\nAddition: {first_num + second_num}")
    print(f"Subtraction: {first_num - second_num}")
    print(f"Multiplication: {first_num * second_num}")
    print(f"Division: {first_num / second_num}")
    print(f"Modulo: {first_num % second_num}")


if __name__ == "__main__":
    main()